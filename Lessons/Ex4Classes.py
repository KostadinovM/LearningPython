#Classes and how they work in Python
class calculator:

    def addition(x,y):
        added = x + y
        print(added)

    def subtraction(x,y):
        sub = x - y;
        print(sub)

    def multiplication(x,y):
        mult = x*y
        print(mult)

    def division(x,y):
        div = x/y
        print(div)

calculator.multiplication(3,5)      #calling the method of a class
